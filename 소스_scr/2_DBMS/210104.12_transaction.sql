--[XII] 트랜젝션 명령어; COMMIT; ROLLBACKL; SAVEPOINT
COMMIT;
DROP TABLE DEPT01;
CREATE TABLE DEPT01    
    AS SELECT * FROM DEPT;
SELECT * FROM DEPT01;--10,20,30,40
DELETE FROM DEPT01;
SELECT * FROM DEPT01;
ROLLBACK;
SELECT * FROM DEPT01;--원상복귀
-----------새로운 트랜젝션 시작 
DELETE FROM DEPT01 WHERE DEPTNO='40';
SELECT * FROM DEPT01;--10,20,30
COMMIT;
-----------새로운 트랜젝션 시작
DELETE FROM DEPT01 WHERE DEPTNO='30';
SELECT * FROM DEPT01;--10,20번부서

SAVEPOINT C1;--C1 시점:10,20 부서존재 
DELETE FROM DEPT01 WHERE DEPTNO='20';
SELECT * FROM DEPT01;--10

SAVEPOINT C2;--C2 시점:10번 부서
DELETE FROM DEPT01 WHERE DEPTNO='10';
SELECT * FROM DEPT01;--데이터 없음

ROLLBACK TO C2;
SELECT * FROM DEPT01;

ROLLBACK TO C1;
SELECT * FROM DEPT01;--10,20
COMMIT;












